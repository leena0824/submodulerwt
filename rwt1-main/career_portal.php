<script>
    var swiper = new Swiper('.swiper-container', {
        loop: true,
        pagination: {
            el: '.swiper-pagination',
            clickable: true,
        },
        navigation: {
            nextEl: '.swiper-button-next',
            prevEl: '.swiper-button-prev',
        },
    });

    swiper.on('slideChange', function () {
        // Remove animation classes from all captions
        document.querySelectorAll('.swiper-caption').forEach(function (caption) {
            caption.classList.remove('active');
            caption.querySelector('.title1').classList.remove('animate', 'animate__animated', 'animate__bounceInDown');
            caption.querySelector('.sub-title').classList.remove('animate', 'animate__animated', 'animate__bounceInRight');
            caption.querySelector('.banner-readmore').classList.remove('animate', 'animate__animated', 'animate__bounceInUp');
        });

        // Add animation classes to the active slide's caption
        var activeSlide = swiper.slides[swiper.activeIndex];
        var activeCaption = activeSlide.querySelector('.swiper-caption');
        if (activeCaption) {
            activeCaption.classList.add('active');
            activeCaption.querySelector('.title1').classList.add('animate', 'animate__animated', 'animate__bounceInDown');
            activeCaption.querySelector('.sub-title').classList.add('animate', 'animate__animated', 'animate__bounceInRight');
            activeCaption.querySelector('.banner-readmore').classList.add('animate', 'animate__animated', 'animate__bounceInUp');
        }
    });

    // Trigger the animation on the first slide
    document.addEventListener('DOMContentLoaded', function () {
        var firstSlideCaption = swiper.slides[swiper.activeIndex].querySelector('.swiper-caption');
        if (firstSlideCaption) {
            firstSlideCaption.classList.add('active');
            firstSlideCaption.querySelector('.title1').classList.add('animate', 'animate__animated', 'animate__bounceInDown');
            firstSlideCaption.querySelector('.sub-title').classList.add('animate', 'animate__animated', 'animate__bounceInRight');
            firstSlideCaption.querySelector('.banner-readmore').classList.add('animate', 'animate__animated', 'animate__bounceInUp');
        }
    });
</script>






